    private void btnsaveclActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveclActionPerformed
        // TODO add your handling code here:
        Client c = new Client();
        int regid = Integer.parseInt(regno.getText());
        c.setRegNo(regid);
        c.setFirstName(fname.getText());
        c.setLastName(lname.getText());
        c.setPhoneNo(phone.getText());
        c.setEmail(mail.getText());
        c.setCategory(cat.getSelectedItem().toString());
        c.setPhoto(FilePath);
        ClientDao cldao = new ClientDao();
        cldao.saveClient(c);
        JOptionPane.showMessageDialog(this, "Client saved");
        DefaultTableModel md = (DefaultTableModel) clienttbl.getModel();
        md.setRowCount(0);
        populateClient();
    }//GEN-LAST:event_btnsaveclActionPerformed

