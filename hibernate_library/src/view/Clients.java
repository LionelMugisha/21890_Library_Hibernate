    private void btnuploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnuploadActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        FileNameExtensionFilter fil = new FileNameExtensionFilter("IMAGES","png","jpeg","jpg","gif");
        file.setFileFilter(fil);
        if(file.showOpenDialog(null)  == JFileChooser.APPROVE_OPTION ) {
            File cfile = file.getSelectedFile();
            FilePath = cfile.getAbsolutePath();
            img = new ImageIcon(FilePath);
            Image image = img.getImage().getScaledInstance(photolbl.getWidth(), photolbl.getHeight(), Image.SCALE_DEFAULT);
            photolbl.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_btnuploadActionPerformed

